# Write a method that takes in a number and returns true if it is a
# power of 2. Otherwise, return false.
#
# Difficulty: medium.

def is_power_of_two?(num)
	iter = 0
	while iter < num
		if 2 ** iter == num
			return true
		end
		iter +=1
	end
	false
end

puts("is_power_of_two?(1) == true: #{is_power_of_two?(1) == true}")
